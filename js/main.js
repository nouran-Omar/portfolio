    initScrollSpy() {
      const sections = document.querySelectorAll(".section");
      const navLinks = document.querySelectorAll(".nav-link");

      const observer = new IntersectionObserver(
        (entries) => {
          entries.forEach((entry) => {
            if (entry.isIntersecting) {
              navLinks.forEach((link) => {
                link.classList.remove("active");
                if (
                  link.getAttribute("href").substring(1) === entry.target.id
                ) {
                  link.classList.add("active");
                }
              });
            }
          });
        },
        { rootMargin: "-50% 0px -50% 0px" },
      );

      sections.forEach((section) => {
        observer.observe(section);
      });
    },

    initTimelineAnimation() {
      const timeline = document.querySelector(".timeline");
      if (!timeline) return;

      const onScroll = () => {
        const timelineRect = timeline.getBoundingClientRect();
        const viewportHeight = window.innerHeight;

        // Calculate the visible part of the timeline
        const visibleTop = Math.max(0, -timelineRect.top);
        const visibleBottom = Math.max(0, timelineRect.bottom - viewportHeight);
        const visibleHeight = timelineRect.height - visibleTop - visibleBottom;

        let percentage =
          (visibleTop / (timelineRect.height - visibleHeight)) * 100;
        percentage = Math.max(0, Math.min(100, percentage)); // Clamp between 0 and 100

        timeline.style.setProperty("--timeline-progress", `${percentage}%`);
      };

      window.addEventListener("scroll", onScroll, { passive: true });
      onScroll(); // Initial call
    },

